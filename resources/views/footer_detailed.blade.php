			<h3>About iReceptor</h3>
			<h5>Our Mission</h5>
            <p>
iReceptor is a data discovery platform that facilitates the curation, analysis and sharing of antibody/B-cell and T-cell receptor repertoires (Adaptive Immune Receptor Repertoire or AIRR-seq data) and related subject and sample metadata from multiple studies across many labs and institutions. We are committed to providing a platform for researchers to increase the value of their data through sharing with the community. This will greatly increase the amount of data available to answer complex questions about the adaptive immune response, accelerating the development of vaccines, therapeutic antibodies against autoimmune diseases, and cancer immunotherapies.
</p>
<h5>Curated Studies Available</h5>
<p>
Users of the iReceptor Gateway can search AIRR-seq data from studies on cancer (for example breast and ovarian), autoimmune diseases (for example MS and SLE), and infectious diseases (for example HIV and influenza).  Working with the COVID-19 and Type 1 Diabetes communities we have recently expanded our curated studies in those areas.

Following the AIRR Data Commons standards we are the only public resource that curates data from single-cell immune-profiling studies.
</p>
			<h5>Our Approach</h5>
			<p>
				The iReceptor Gateway integrates large, distributed, AIRR-seq data repositories, following standards for sharing and interoperability developed by the
				<a href="http://airr-community.org/">Adaptive Immune Receptor Repertoire (AIRR) Community</a>.
				The main goal of iReceptor is to connect this distributed network of AIRR-seq repositories into an AIRR Data Commons, allowing for the query, analysis, and visualization of AIRR-seq data across multiple projects, labs, and institutions.
			</p>
			<p>Present functionalities include:</p>
			<ul>
				<li><strong>Search</strong> for <strong>Repertoires</strong> satisfying certain metadata (e.g. find all AIRR-seq repertoires from ovarian cancer studies)</li>
				<li><strong>Search</strong> selected repertoires for <strong>Sequence</strong> characteristics such as a specific CDR3 sequence or particular V, D, and J genes and alleles</li>
				<li><strong>Search</strong> selected repertoires for <strong>Clone</strong> attributes such as a specific CDR3 sequence or particular V, D, and J genes and alleles</li>
				<li><strong>Search</strong> selected repertoires for <strong>Cell</strong> characteristics such as Gene Expression Levels.</li>
				<li><strong>Download Sequences, Clones, Cells</strong> and their related Repertoire metadata in AIRR compliant file formats, easily importable to other AIRR-seq analysis tools</li>
			    <li><strong>Analyze Sequences, Clones, Cells</strong> and their related Repertoire metadata using <strong>community built analysis tools</strong>.</li>
			</ul>
			<p>iReceptor was a leading member of the CIHR/EU Horizon 2020 funded <a href="http://www.ireceptor-plus.com/">iReceptor Plus Consortium</a>.</p>

			<p>For more information, see the <a href="http://www.ireceptor.org/">iReceptor public website</a> or refer to the paper <a href="http://doi.org/10.1111/imr.12666">iReceptor: a platform for querying and analyzing antibody/B-cell and T-cell receptor repertoire data across federated repositories</a>.  If you are interested in hosting a data repository or if you want to provide feedback on this developing community resource, email <a href="mailto:support@ireceptor.org">support@ireceptor.org</a>.</p>

			<h5>Acknowledging iReceptor</h5>
			<p>If you find iReceptor useful in your research, please acknowledge the iReceptor Platform's contribution by citing the iReceptor paper by Corrie et al. <a href="http://doi.org/10.1111/imr.12666">iReceptor: a platform for querying and analyzing antibody/B-cell and T-cell receptor repertoire data across federated repositories</a>, Immunological Reviews, Volume 284:24-41 (2018).</p>
